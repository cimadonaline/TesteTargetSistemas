INDICE = 12
SOMA = 0
K = 1

while K < INDICE:
    K += 1  # Incrementa K
    SOMA += K  # Adiciona K à SOMA

print(SOMA)  # Imprime a SOMA

#Explicação

#O valor final de SOMA será 77
#O loop 'enquanto' irá somar todos os números entre do 'indice' (que é 12) e o 'K'(sendo 1), sendo assim todos os números entre 2 a 12.